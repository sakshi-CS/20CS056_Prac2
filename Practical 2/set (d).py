# Write a Python program to find maximum and the minimum value in a set.

def MAX(sets):
    return (max(sets))

# Driver Code
sets = set([5, 20, 65, 1, 21, 3, 10, 76, 59])
print(MAX(sets))

# Python code to get the minimum element from a set
def MIN(sets):
    return (min(sets))

# Driver Code
sets = set([1, 15, 8, 19, 4, 26])
print(MIN(sets))